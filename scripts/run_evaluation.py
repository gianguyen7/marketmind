"""Run full evaluation: metrics, calibration plots, comparison charts.

Loads saved predictions from outputs/predictions/ (generated by run_training.py).
"""

import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

import numpy as np
import pandas as pd

from src.evaluation.compare_models import compare_all_models, evaluate_predictions
from src.evaluation.reporting import (
    plot_calibration_curves,
    plot_model_comparison,
    plot_prediction_distributions,
    save_comparison_table,
)


def load_predictions(pred_dir: str = "outputs/predictions") -> dict:
    """Load saved predictions from JSON files."""
    results = {}
    pred_path = Path(pred_dir)
    if not pred_path.exists():
        print(f"No predictions directory found at {pred_dir}. Run training first.")
        return results

    for json_file in sorted(pred_path.glob("*.json")):
        data = json.loads(json_file.read_text())
        preds = {k: np.array(v) for k, v in data.items()}
        # compare_all_models expects (model, preds) tuples — model is unused for eval
        results[json_file.stem] = (None, preds)
        print(f"  Loaded predictions: {json_file.stem}")

    return results


def main():
    print("=" * 60)
    print("MarketMind Evaluation")
    print("=" * 60)

    results = load_predictions()
    if not results:
        print("No predictions found. Run `python scripts/run_training.py` first.")
        return

    # Compare on test set
    print("\n--- Test Set ---")
    comparison = compare_all_models(results, split="test")
    print(comparison.to_string(index=False))
    save_comparison_table(comparison)

    # Compare on val set
    print("\n--- Validation Set ---")
    val_comparison = compare_all_models(results, split="val")
    print(val_comparison.to_string(index=False))

    # Generate plots
    print("\nGenerating plots...")
    Path("outputs/figures").mkdir(parents=True, exist_ok=True)
    plot_calibration_curves(results, "test", output_path="outputs/figures/calibration_test.html")
    plot_calibration_curves(results, "val", output_path="outputs/figures/calibration_val.html")
    plot_model_comparison(comparison, "brier_score", output_path="outputs/figures/brier_comparison.html")
    plot_model_comparison(comparison, "log_loss", output_path="outputs/figures/logloss_comparison.html")
    plot_prediction_distributions(results, "test", output_path="outputs/figures/pred_distributions.html")

    print("\nEvaluation complete. See outputs/figures/ and outputs/tables/")


if __name__ == "__main__":
    main()
